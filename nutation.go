//中精度章动计算,t是世纪数
func nutation2(t float64) vector3 {
	var a float64
	t2 := t * t
	var dL, dE float64
	B := nutB
	for i := 0; i < len(B); i += 5 {
		c := B[i] + B[i+1]*t + B[i+2]*t2
		if i == 0 {
			a = -1.742 * t
		} else {
			a = 0
		}
		dL += (B[i+3] + a) * math.Sin(c)
		dE += B[i+4] * math.Cos(c)
	}
	return vector3{dL / 100 / rad, dE / 100 / rad, 0} //黄经章动,交角章动
}

